package pe.edu.entity;

/**
 *
 * @author LENOVO
 */
public class Habitacion {
    private int id_hab;
    private int id_tipo_hab;
    private String tipo_nombre;
    private double precio;
    private String estado;
    private int num_hab;
    private int id_usuario;

    public int getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(int id_usuario) {
        this.id_usuario = id_usuario;
    }
    

    public int getId_hab() {
        return id_hab;
    }

    public void setId_hab(int id_hab) {
        this.id_hab = id_hab;
    }

    public int getId_tipo_hab() {
        return id_tipo_hab;
    }

    public void setId_tipo_hab(int id_tipo_hab) {
        this.id_tipo_hab = id_tipo_hab;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public int getNum_hab() {
        return num_hab;
    }

    public void setNum_hab(int num_hab) {
        this.num_hab = num_hab;
    }

    public String getTipo_nombre() {
        return tipo_nombre;
    }

    public void setTipo_nombre(String tipo_nombre) {
        this.tipo_nombre = tipo_nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }
    
    
}
